# x str akan keluar '9'/"9" 
# Kutipan string bisa menggunakan tunggal dan ganda
x = str(9)      # str / string 
y = int(9)      # int / integer
z = float(9)    # float adalah pecahan

print("str", x)
print("int", y)
print("float", z)


