# Memodifikasi string 
# Huruf besar

# Contoh
# Metode upper() mengembalikkan string dalam huruf besar;

a = "haii Kawan"
print(a.upper())  # nama variable lalu masukan kata kunci upper()

# Metode lower() untuk mengubah semua huruf menjadi huruf kecil
b = "HALOOO KAWANKU"
print("          ini adalah contoh")
print(b.lower())

# Hapus spasi putih
# spasi kosong adalah spasi sebelum dan/ setelah teks sebenarnya, 
# dan seringkali anda ingin menghapus spasi ini.
# 
# Contoh 
# metode ini strip() menghapus spasi dari awal atau akhir;

x ="       Hallo, Temann        " 
print(x.strip())

# Ganti Huruf
# Menggunakan metode replace() menggantikan string dengan string lain;

y = "tuskul"
print(y.replace("t","Y"))

# split pemisah
# Metode split() membagi string menjadi substring jika menemukan contoh pemisah;

z = "Makasih, semua"
print(z.split(","))


