# Mengiris
# Anda dapat mengembalikkan rentang karakter dengan menggunakan sintaks irisan.
# Tentukan indeks awal dan akhir, dipisahkan dengan titik dua, untuk mengembalikan bagian dari string.

# contoh 
# kita bisa mengambil posisi ke 0 sampai ke 7 tapi posisi 7 tidak termasuk;
b = "Halloo kawan"
print(b[1:7])


# iris dari awal 
# Dengan mengabaikan indeks awal,rentang akan dimulai dari karakter pertama;

# contoh
# memotong karakter dari awal sampai posisi 5 tapi posisi 5 tidak dihutung;
a = "Kawan lama"
print(a[:5])



# Iris sampai akhir
# Dengan mengabaikan indeks akhir, rentang akan menuju ke akhir;
# contoh
# mengambil karakter dari posisi ke2 sampai akhir
c = "Selamat Datang"
print(c[2:])



